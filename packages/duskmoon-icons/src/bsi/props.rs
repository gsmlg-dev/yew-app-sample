use yew::prelude::*;

#[derive(Properties, Clone, PartialEq)]
pub struct IconProps {
    #[prop_or_default]
    pub id: AttrValue,
    /// CSS classes to add to the anchor element (optional).
    #[prop_or_default]
    pub class: Classes,
    #[prop_or(AttrValue::from("1em"))]
    pub size: AttrValue,
    #[prop_or(AttrValue::from("currentColor"))]
    pub color: AttrValue,
    #[prop_or_default]
    pub style: AttrValue,
    #[prop_or_default]
    pub name: AttrValue,
}
